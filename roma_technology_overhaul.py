def validate() -> list[str]:
    errors: list[str] = []
    if len(TECH_TREE) != TECH_COUNT:
        errors.append(f"expected {TECH_COUNT} technologies, got {len(TECH_TREE)}")
    if len(set(TECH_TREE)) != len(TECH_TREE):
        errors.append("duplicate technology IDs")
    for tech_id, spec in TECH_TREE.items():
        if not spec.get("name"):
            errors.append(f"{tech_id}: missing name")
        if not spec.get("desc"):
            errors.append(f"{tech_id}: missing description")
        if tech_id not in SCIENCE_TECH_META:
            errors.append(f"{tech_id}: missing meta")
        for req in spec.get("prereq", []):
            if req not in TECH_TREE:
                errors.append(f"{tech_id}: unknown prerequisite {req}")
    # Cycle check.
    indegree = {key: 0 for key in TECH_TREE}
    outgoing = {key: [] for key in TECH_TREE}
    for key, spec in TECH_TREE.items():
        for req in spec.get("prereq", []):
            indegree[key] += 1
            outgoing[req].append(key)
    queue = [key for key, value in indegree.items() if value == 0]
    visited = 0
    while queue:
        key = queue.pop()
        visited += 1
        for child in outgoing[key]:
            indegree[child] -= 1
            if indegree[child] == 0:
                queue.append(child)
    if visited != len(TECH_TREE):
        errors.append("technology prerequisites contain a cycle")
    for era in range(1, 6):
        count = sum(1 for row in SCIENCE_TECH_META.values() if row.get("era") == era)
        if count != 20:
            errors.append(f"era {era}: expected 20 technologies, got {count}")
    if set(TECH_QUOTES) != set(TECH_TREE):
        errors.append("TECH_QUOTES IDs do not match TECH_TREE IDs")
    unknown_building_techs = sorted({
        req for reqs in BUILDING_TECH_PATCH.values() for req in reqs if req not in TECH_TREE
    })
    if unknown_building_techs:
        errors.append(f"unknown building technologies: {unknown_building_techs}")
    unknown_artillery_techs = sorted({
        req for row in ARTILLERY_REQUIREMENTS.values() for req in row.get("tech", []) if req not in TECH_TREE
    })
    if unknown_artillery_techs:
        errors.append(f"unknown artillery technologies: {unknown_artillery_techs}")
    unknown_wonder_techs = sorted({
        req for reqs in WONDER_REQUIREMENTS.values() for req in reqs if req not in TECH_TREE
    })
    if unknown_wonder_techs:
        errors.append(f"unknown wonder technologies: {unknown_wonder_techs}")
    return errors


_VALIDATION_ERRORS = validate()
if _VALIDATION_ERRORS:
    raise RuntimeError("Scientia et Ars validation failed: " + "; ".join(_VALIDATION_ERRORS))
