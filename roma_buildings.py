def _type_matches(city: dict, allowed: list[str]) -> bool:
    if not allowed: return True
    text = (str(city.get("type", "")) + " " + str(city.get("name", ""))).lower()
    aliases = {
        "столица": ("столиц", "roma"), "порт": ("порт", "мор", "гаван"), "речной": ("реч",),
        "торговый": ("торг",), "крепость": ("креп",), "земледельческий": ("землед", "сель"),
        "рудники": ("руд", "шахт"), "ремесленный": ("ремес",), "религиозный": ("религ", "культ", "свящ"),
        "пограничный": ("погранич",), "племенной": ("плем",), "осадный": ("осад",),
        "культурный": ("культур",), "военный": ("воен",), "административный": ("администр",),
    }
    for kind in allowed:
        needles = aliases.get(str(kind).lower(), (str(kind).lower(),))
        if any(n and n in text for n in needles): return True
    return False


def owned_buildings(player: Any) -> set[str]:
    state = _dict(getattr(player, "city_system", {}))
    result: set[str] = set()
    for city in _dict(state.get("cities")).values():
        if isinstance(city, dict) and city.get("active", True):
            result.update(ensure_city(city)["buildings"])
    return result


def eligibility(player: Any, province: dict, city: dict, building: dict, ctx: dict | None = None) -> tuple[bool, str]:
    ensure_city(city)
    bid = str(building.get("id", ""))
    if bid in city["buildings"]: return False, "уже построено"
    if bid in city["building_project_cooldowns"]: return False, "город недавно отклонил проект"
    if building.get("unique") and bid in owned_buildings(player): return False, "уникальное сооружение уже существует"
    required = set(str(x) for x in _list(building.get("required_techs")))
    missing = required - _techs(player)
    if missing: return False, "не исследованы технологии: " + ", ".join(sorted(missing))
    for dep in _list(building.get("required_buildings")):
        if str(dep) not in city["buildings"]: return False, f"требуется {dep}"
    provinces = {str(x).lower() for x in _list(building.get("provinces"))}
    if provinces and str(province.get("name", "")).lower() not in provinces: return False, "недоступно в этой провинции"
    if not _type_matches(city, _list(building.get("city_types"))): return False, "неподходящий тип города"
    religion = str(building.get("religion") or "")
    if religion and _religion(player) != religion: return False, f"требуется религия {religion}"
    return True, ""


def _stable_random(*parts: Any) -> float:
    raw = json.dumps(parts, ensure_ascii=False, sort_keys=True, default=str).encode("utf-8")
    return int.from_bytes(hashlib.sha256(raw).digest()[:8], "big") / float(2**64 - 1)


def project_score(player: Any, province: dict, city: dict, building: dict, ctx: dict | None = None) -> float:
    ok, _ = eligibility(player, province, city, building, ctx)
    if not ok: return -1e9
    effects = _dict(building.get("effects")); metrics = _dict(effects.get("city_metrics"))
    score = 8.0
    # Municipal need: a weak city strongly asks for relevant public works.
    for key, weight in (("food", 0.55), ("health", 0.45), ("order", 0.42), ("infrastructure", 0.38), ("defense", 0.30), ("trade", 0.22), ("prosperity", 0.22), ("loyalty", 0.34), ("culture", 0.15)):
        bonus = max(0.0, _f(metrics.get(key, 0.0)))
        need = max(0.0, 68.0 - _f(city.get(key, 50.0), 50.0))
        score += bonus * need * weight / 12.0
    score += max(0, _i(effects.get("gold_per_turn", 0))) * 0.12
    score += max(0, _i(effects.get("grain_per_turn", 0))) * 0.08
    score += sum(max(0.0, _f(v)) for v in _dict(effects.get("resource_yields")).values()) * 1.5
    score -= max(0, _i(building.get("upkeep", 0))) * 0.25
    score += _stable_random(getattr(player, "turn", 0), province.get("name"), city.get("name"), building.get("id")) * 8.0
    return score


def select_projects(player: Any, cities: list[tuple[dict, dict]], ctx: dict | None = None, limit: int = 1) -> list[tuple[dict, dict, dict]]:
    candidates=[]
    for province, city in cities:
        ensure_city(city)
        for building in BUILDING_CATALOG.values():
            score=project_score(player, province, city, building, ctx)
            if score>-1e8: candidates.append((score, province, city, building))
    candidates.sort(key=lambda row: row[0], reverse=True)
    selected=[]; used_cities=set(); used_unique=set()
    for _, province, city, building in candidates:
        key=f"{province.get('name')}::{city.get('name')}"
        if key in used_cities: continue
        bid=str(building.get("id", ""))
        if building.get("unique") and bid in used_unique: continue
        selected.append((province, city, building)); used_cities.add(key)
        if building.get("unique"): used_unique.add(bid)
        if len(selected)>=max(0,limit): break
    return selected


def _dynamic_base_cost(player: Any, building: dict, ctx: dict | None = None) -> int:
    base = max(0, _i(building.get("cost_gold", 0)))
    dynamic = base
    fn = _dict(ctx).get("game_price")
    if callable(fn):
        try:
            dynamic = max(0, _i(fn(player, base, market=True, minimum=0)))
        except Exception:
            dynamic = base
    # Один только инфляционный коэффициент не должен превращать сооружение
    # ценой в несколько тысяч в проект на десятки тысяч золота.
    return min(MAX_PROJECT_TOTAL_GOLD, dynamic)


def _resource_state(player: Any, ctx: dict | None = None) -> dict:
    module=_resource_module(ctx)
    if module is not None and hasattr(module,"ensure_state"):
        try:
            context_fn=_dict(ctx).get("resource_economy_context")
            context=context_fn(player) if callable(context_fn) else {}
            return module.ensure_state(player,context)
        except Exception: pass
    state=getattr(player,"resource_economy",None)
    return state if isinstance(state,dict) else {}


def quote_project(player: Any, building: dict, ctx: dict | None = None) -> dict:
    state=_resource_state(player,ctx)
    stock=_dict(state.get("stockpiles")); prices=_dict(state.get("market_prices"))
    module=_resource_module(ctx); catalogue=getattr(module,"RESOURCE_CATALOG",{}) if module is not None else {}
    required={str(k):max(0.0,_f(v)) for k,v in _dict(building.get("build_resources")).items() if _f(v)>0}
    missing={k:max(0.0,amount-_f(stock.get(k,0.0))) for k,amount in required.items()}
    missing={k:v for k,v in missing.items() if v>1e-6}
    market_cost=0.0
    for key,amount in missing.items():
        spec=_dict(catalogue.get(key)); base=_f(spec.get("base_price",10.0),10.0)
        market_cost += amount * max(0.1,_f(prices.get(key,base),base))
    grain_required=max(0,_i(building.get("grain_cost",0)))
    grain_have=max(0,_i(getattr(player,"grain",0)))
    grain_missing=max(0,grain_required-grain_have)
    grain_price=5.0
    economy=_dict(getattr(player,"economy",{})); statement=_dict(economy.get("last_statement")); grain=_dict(statement.get("grain"))
    grain_price=max(2.0,_f(grain.get("price",grain_price),grain_price)*4.0)
    market_cost += grain_missing*grain_price
    base_gold = _dynamic_base_cost(player, building, ctx)
    uncapped_emergency_cost = int(math.ceil(market_cost * EMERGENCY_PURCHASE_MARKUP))
    # Главный балансирующий инвариант: постройка с нулевыми запасами, включая
    # срочную закупку всех материалов и зерна, всегда укладывается в 5000.
    emergency_room = max(0, MAX_PROJECT_TOTAL_GOLD - base_gold)
    emergency_cost = min(uncapped_emergency_cost, emergency_room)
    total_with_purchase = min(MAX_PROJECT_TOTAL_GOLD, base_gold + emergency_cost)
    return {
        "base_gold": base_gold, "resources": required, "grain": grain_required, "missing_resources": missing,
        "missing_grain": grain_missing, "emergency_purchase": emergency_cost,
        "total_with_purchase": total_with_purchase, "uncapped_total_with_purchase": base_gold + uncapped_emergency_cost,
        "cost_cap_applied": base_gold + uncapped_emergency_cost > MAX_PROJECT_TOTAL_GOLD,
        "stock": stock, "prices": prices,
    }


def resource_name(key: str, ctx: dict | None = None) -> str:
    module=_resource_module(ctx); spec=_dict(getattr(module,"RESOURCE_CATALOG",{}).get(key)) if module is not None else {}
    return f"{spec.get('icon','•')} {spec.get('name',key)}"


def effects_text(building: dict, ctx: dict | None = None) -> str:
    effects=_dict(building.get("effects")); parts=[]
    for key,label in (("gold_per_turn","золота/ход"),("grain_per_turn","зерна/ход"),("science_per_turn","науки/ход"),("faith_per_turn","веры/ход")):
        value=_i(effects.get(key,0))
        if value: parts.append(f"+{value} {label}")
    for key,value in _dict(effects.get("resource_yields")).items():
        if abs(_f(value))>1e-6: parts.append(f"{_f(value):+g} {resource_name(key,ctx)}/ход")
    for key,value in _dict(effects.get("city_metrics")).items():
        if value: parts.append(f"{key} {int(value):+d}")
    pressure = _dict(building.get("religion_pressure"))
    if pressure:
        parts.append("религиозное давление " + ", ".join(f"{key} +{_f(value):g}" for key, value in pressure.items()))
    integrity = _i(building.get("religion_integrity", 0))
    if integrity:
        parts.append(f"Integrity +{integrity}")
    return "; ".join(parts) or "долгосрочное развитие города"


