def execution_blockers(player: Any, city: dict, option: dict, ctx: dict | None = None) -> list[str]:
    """Return human-readable reasons why a municipal option cannot run.

    ``build_buy`` is an emergency procurement contract: only its quoted gold
    price is required.  Physical resources listed in an old/stale saved event
    must not block it, because the contractor delivers the missing materials.
    """
    action = str(option.get("project_action", ""))
    req = _dict(option.get("requires"))
    if action in {"defer", "decline"}:
        return []

    blockers: list[str] = []
    gold_have = max(0, _i(getattr(player, "gold", 0)))
    gold_need = max(0, _i(req.get("gold", 0)))
    if gold_have < gold_need:
        blockers.append(f"золото: нужно {gold_need}, есть {gold_have}")

    grain_have = max(0, _i(getattr(player, "grain", 0)))
    grain_need = max(0, _i(req.get("grain", 0)))
    if grain_have < grain_need:
        blockers.append(f"зерно: нужно {grain_need}, есть {grain_have}")

    # Emergency procurement pays for missing materials in gold.  This explicit
    # branch also repairs projects already stored in older saves where the
    # build_buy option accidentally retained a non-empty resources mapping.
    if action != "build_buy":
        state = _resource_state(player, ctx)
        stock = _dict(state.get("stockpiles"))
        for key, amount in _dict(req.get("resources")).items():
            need = max(0.0, _f(amount))
            have = max(0.0, _f(stock.get(key, 0.0)))
            if have + 1e-6 < need:
                blockers.append(f"{resource_name(str(key), ctx)}: нужно {need:g}, есть {have:g}")
    return blockers


def can_execute(player: Any, city: dict, option: dict, ctx: dict | None = None) -> bool:
    return not execution_blockers(player, city, option, ctx)


def _apply_city_metrics(city: dict, building: dict) -> None:
    metrics=_dict(_dict(building.get("effects")).get("city_metrics"))
    for key,value in metrics.items():
        if key in CITY_METRICS:
            city[key]=max(0,min(100,_i(city.get(key,50),50)+_i(value)))


def execute_project(player: Any, province: dict, city: dict, option: dict, ctx: dict | None = None) -> str:
    ensure_city(city); bid=str(option.get("building_id", "")); building=BUILDING_CATALOG.get(bid)
    if not isinstance(building,dict): return "Проект отсутствует в строительном реестре."
    action=str(option.get("project_action", ""))
    if action=="defer":
        city["building_project_cooldowns"][bid]=PROJECT_COOLDOWN_DEFERRED
        return f"Проект «{building['name']}» отложен."
    if action=="decline":
        city["building_project_cooldowns"][bid]=PROJECT_COOLDOWN_DECLINED
        return f"Муниципию отказано в строительстве «{building['name']}»."
    if not can_execute(player,city,option,ctx): return "Недостаточно средств или материалов для исполнения проекта."
    req=_dict(option.get("requires")); player.gold=_i(getattr(player,"gold",0))-_i(req.get("gold",0)); player.grain=_i(getattr(player,"grain",0))-_i(req.get("grain",0))
    state=_resource_state(player,ctx); stock=_dict(state.get("stockpiles"))
    if action=="build_stock":
        for key,amount in _dict(req.get("resources")).items(): stock[key]=max(0.0,_f(stock.get(key,0.0))-_f(amount))
    elif action=="build_buy":
        # Existing material is used first; the rest is delivered by the emergency contract.
        for key,amount in _dict(building.get("build_resources")).items(): stock[key]=max(0.0,_f(stock.get(key,0.0))-_f(amount))
        player.grain=max(0,_i(getattr(player,"grain",0))-min(_i(getattr(player,"grain",0)),_i(building.get("grain_cost",0))))
    city["buildings"].append(bid); city["buildings"]=list(dict.fromkeys(city["buildings"])); city["last_building_turn"]=_i(getattr(player,"turn",1),1)
    module=_resource_module(ctx)
    push=getattr(module,"_push_legacy_metals",None) if module is not None else None
    if callable(push):
        try: push(player,state)
        except Exception: pass
    _apply_city_metrics(city,building)
    # Empire-wide effects of the four wonders are applied once at completion.
    state_city=_dict(getattr(player,"city_system",{})); all_cities=[x for x in _dict(state_city.get("cities")).values() if isinstance(x,dict) and x.get("active")]
    global_effects={"colosseum":{"order":3},"pantheon":{"culture":3},"great_lighthouse":{"trade":4},"via_appia_terminus":{"infrastructure":3}}
    for target in all_cities:
        if bid=="great_lighthouse" and not _type_matches(target,["порт"]): continue
        for key,value in _dict(global_effects.get(bid)).items(): target[key]=max(0,min(100,_i(target.get(key,50),50)+_i(value)))
    return f"{building['latin']} ({building['name']}) построено в городе {city.get('name')}. Постоянный эффект: {effects_text(building,ctx)}."


def economy_snapshot(player: Any, ctx: dict | None = None) -> dict:
    state=_dict(getattr(player,"city_system",{})); cities=[x for x in _dict(state.get("cities")).values() if isinstance(x,dict) and x.get("active")]
    result={
        "gold_per_turn":0,"grain_per_turn":0,"science_per_turn":0,"faith_per_turn":0,
        "upkeep":0,"resource_output":{},"resource_input":{},"sector_bonuses":{},
        "province_sector_bonuses":{},"building_count":0,
        "religion_pressure":{},"religion_integrity":{},
    }
    for city in cities:
        ensure_city(city); province=str(city.get("province", "")); psec=result["province_sector_bonuses"].setdefault(province,{k:0.0 for k in SECTORS})
        for bid in city["buildings"]:
            building=BUILDING_CATALOG.get(bid)
            if not isinstance(building,dict): continue
            result["building_count"]+=1; result["upkeep"]+=_i(building.get("upkeep",0))
            effects=_dict(building.get("effects"))
            for key in ("gold_per_turn","grain_per_turn","science_per_turn","faith_per_turn"): result[key]+=_i(effects.get(key,0))
            province_pressure=result["religion_pressure"].setdefault(province,{})
            for religion_key,value in _dict(building.get("religion_pressure")).items():
                province_pressure[str(religion_key)]=province_pressure.get(str(religion_key),0.0)+_f(value)
            integrity=_i(building.get("religion_integrity",0))
            if integrity:
                result["religion_integrity"][province]=result["religion_integrity"].get(province,0)+integrity
            for key,value in _dict(effects.get("sector_bonuses")).items():
                result["sector_bonuses"][key]=result["sector_bonuses"].get(key,0.0)+_f(value); psec[key]=psec.get(key,0.0)+_f(value)
            for key,value in _dict(effects.get("resource_yields")).items():
                value=_f(value)
                target="resource_output" if value>=0 else "resource_input"
                result[target][key]=result[target].get(key,0.0)+abs(value)
    return result


def apply_passive_civic_yields(player: Any, ctx: dict | None = None) -> dict:
    state=_dict(getattr(player,"city_system",{})); turn=_i(getattr(player,"turn",1),1)
    if _i(state.get("last_building_yield_turn",0),0)>=turn: return {"science":0,"faith":0}
    snap=economy_snapshot(player,ctx); science=_i(snap.get("science_per_turn",0)); faith=_i(snap.get("faith_per_turn",0))
    if science: player.science_points=max(0,_i(getattr(player,"science_points",0))+science)
    if faith: player.faith=max(0,_i(getattr(player,"faith",0))+faith)
    state["last_building_yield_turn"]=turn
    return {"science":science,"faith":faith}


def catalogue_audit() -> list[str]:
    errors=[]
    if len(BUILDING_CATALOG)!=200: errors.append(f"ожидалось 200 зданий, получено {len(BUILDING_CATALOG)}")
    for bid,row in BUILDING_CATALOG.items():
        if row.get("id")!=bid: errors.append(f"{bid}: несовпадающий id")
        if _i(row.get("cost_gold",-1),-1)<0: errors.append(f"{bid}: отрицательная цена")
        for dep in _list(row.get("required_buildings")):
            if dep not in BUILDING_CATALOG: errors.append(f"{bid}: неизвестное здание-предпосылка {dep}")
    return errors
