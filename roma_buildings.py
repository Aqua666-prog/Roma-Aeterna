def ensure_city(city: dict) -> dict:
    buildings = city.get("buildings")
    if isinstance(buildings, dict):
        buildings = list(buildings)
    if not isinstance(buildings, list):
        buildings = []
    city["buildings"] = [str(x) for x in buildings if str(x) in BUILDING_CATALOG]
    city["buildings"] = list(dict.fromkeys(city["buildings"]))
    cooldowns = city.get("building_project_cooldowns")
    if not isinstance(cooldowns, dict): cooldowns = {}
    city["building_project_cooldowns"] = {str(k): _i(v, 0, 0) for k, v in cooldowns.items() if _i(v, 0) > 0}
    city.setdefault("last_building_turn", 0)
    return city


def tick_city(city: dict) -> None:
    ensure_city(city)
    city["building_project_cooldowns"] = {
        key: turns - 1 for key, turns in city["building_project_cooldowns"].items() if turns - 1 > 0
    }


